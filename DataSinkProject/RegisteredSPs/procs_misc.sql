USE DataSinkDB
GO


DROP PROCEDURE IF EXISTS PROC_ALL_ATTRIB
GO
CREATE PROCEDURE PROC_ALL_ATTRIB
AS
  SELECT *
  FROM DatumAttrib
GO


DROP PROCEDURE IF EXISTS PROC_ALL_DATUM
GO
CREATE PROCEDURE PROC_ALL_DATUM
AS 
  SELECT *
  FROM Datum
GO


DROP PROCEDURE IF EXISTS PROC_ALL_DATASET
GO
CREATE PROCEDURE PROC_ALL_DATASET
AS
  SELECT DISTINCT DA.Value
  FROM DatumAttrib DA
  WHERE DA.Tag="DataSet"
GO


DROP PROCEDURE IF EXISTS PROC_COUNT_DATAPOINTS
GO
CREATE PROCEDURE PROC_COUNT_DATAPOINTS
AS
SELECT COUNT(PkDatumId)
FROM Datum
GO


DROP PROCEDURE IF EXISTS PROC_COUNT_ATTRIB
GO
CREATE PROCEDURE PROC_COUNT_ATTRIB
AS
SELECT COUNT(PkDatumAttribId)
FROM DatumAttrib
GO